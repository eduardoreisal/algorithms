"""
What is a Tuple?
A tuple is an immutable sequence of Python objects, Tuples
are also comparable and hashable
"""

new_tuple = ('a', 'b', 'c', 'd', 'e')
print(new_tuple)

# To create a Tuple with onlu one element it's necessary to add a final comma
new_tuple2 = ('a',)
print(new_tuple2)

# To create an empty tuple
new_tuple3 = tuple()
